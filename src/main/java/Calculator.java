public class Calculator {

    public static int addingAtoB(int a, int b) {
        return a+b;
    }

    public static int subtractingAfromB(int a, int b) {
        return b-a;
    }

    public static int multiplying(int a, int b) {
        return a*b;
    }

    public static double dividing (double a, double b) {
        return a/b;
    }
}
